// Helper functions
export function getServerBySlug(slug: string): MCPServer | undefined {
  return servers.find(s => s.slug === slug);
}

export function getServersByCategory(categorySlug: string): MCPServer[] {
  return servers.filter(s => s.categories.includes(categorySlug));
}

export function getServersByIntegration(integrationSlug: string): MCPServer[] {
  return servers.filter(s => s.integrations.includes(integrationSlug));
}

export function getFeaturedServers(): MCPServer[] {
  return servers.filter(s => s.featured);
}

export function getOfficialServers(): MCPServer[] {
  return servers.filter(s => s.official);
}

export function searchServers(query: string): MCPServer[] {
  const q = query.toLowerCase();
  return servers.filter(s => 
    s.name.toLowerCase().includes(q) ||
    s.description.toLowerCase().includes(q) ||
    s.author.toLowerCase().includes(q) ||
    s.categories.some(c => c.includes(q))
  );
}

export function getRelatedServers(server: MCPServer, limit = 5): MCPServer[] {
  return servers
    .filter(s => s.slug !== server.slug)
    .filter(s => s.categories.some(c => server.categories.includes(c)))
    .slice(0, limit);
}

export function getCategoriesWithCounts(): Category[] {
  return categories.map(cat => ({
    ...cat,
    count: servers.filter(s => s.categories.includes(cat.slug)).length
  }));
}

export function getIntegrationsWithCounts(): Integration[] {
  return integrations.map(int => ({
    ...int,
    count: servers.filter(s => s.integrations.includes(int.slug)).length
  }));
}

export function generateComparisons(): Array<{ serverA: MCPServer; serverB: MCPServer; slug: string }> {
  const seen = new Set<string>();
  const comparisons: Array<{ serverA: MCPServer; serverB: MCPServer; slug: string }> = [];

  function addPair(a: MCPServer, b: MCPServer) {
    const slugParts = [a.slug, b.slug].sort();
    const slug = `${slugParts[0]}-vs-${slugParts[1]}`;
    if (seen.has(slug)) return;
    seen.add(slug);
    const serverA = a.slug === slugParts[0] ? a : b;
    const serverB = a.slug === slugParts[0] ? b : a;
    comparisons.push({ serverA, serverB, slug });
  }

  // 1. Top 30 featured/popular servers — all pairs (up to 435)
  const featured = getFeaturedServers();
  const official = getOfficialServers();
  const topServers = [...new Map([...featured, ...official].map(s => [s.slug, s])).values()].slice(0, 30);
  
  for (let i = 0; i < topServers.length; i++) {
    for (let j = i + 1; j < topServers.length; j++) {
      addPair(topServers[i], topServers[j]);
    }
  }

  // 2. Category-specific comparisons — top servers within each category
  for (const cat of categories) {
    const catServers = getServersByCategory(cat.slug).slice(0, 8);
    for (let i = 0; i < catServers.length; i++) {
      for (let j = i + 1; j < catServers.length; j++) {
        addPair(catServers[i], catServers[j]);
      }
    }
  }

  return comparisons;
}